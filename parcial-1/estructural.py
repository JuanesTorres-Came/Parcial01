# Este ejercicio simula el procesamiento de una lista de datos, como sumar ventas diarias o calcular totales en reportes financieros.

def suma_lista(lista):
    resultado = 0
    for i in lista: 
        resultado = resultado + i
    if resultado > 100:
        return 'mayor'
    else:
        return 'menor'

lista=([70,50])
print(suma_lista(lista))